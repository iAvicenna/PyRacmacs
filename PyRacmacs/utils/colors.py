class RGB(Color):
    def hex_format(self):
        '''Returns color in hex format'''
        return '#{:02X}{:02X}{:02X}'.format(self.red,self.green,self.blue)



def clamp(x):
  return int(max(0, min(x, 255)))

def rgb_to_hex(r,g,b):
  return "#{0:02x}{1:02x}{2:02x}".format(clamp(r), clamp(g), clamp(b))


